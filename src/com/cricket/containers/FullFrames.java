package com.cricket.containers;

public class FullFrames {
	
	private int highlight,preHighlight,secondHighlight;
	private String whichhighlight;

	public int getHighlight() {
		return highlight;
	}

	public void setHighlight(int highlight) {
		this.highlight = highlight;
	}

	public int getPreHighlight() {
		return preHighlight;
	}

	public void setPreHighlight(int preHighlight) {
		this.preHighlight = preHighlight;
	}

	public int getSecondHighlight() {
		return secondHighlight;
	}

	public void setSecondHighlight(int secondHighlight) {
		this.secondHighlight = secondHighlight;
	}

	public String getWhichhighlight() {
		return whichhighlight;
	}

	public void setWhichhighlight(String whichhighlight) {
		this.whichhighlight = whichhighlight;
	}
	
}
